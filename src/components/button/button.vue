<template>
    <button :class="className">
        <template v-if="text">
            {{ text }}
        </template>
        <template v-else>
            <slot />
        </template>
    </button>
</template>
<script>
import Base from "../base/base.vue";
export default {

    extends: Base,

    props: {
        text: {
            type: String,
            default: ""
        },
        primary: {
            type: Boolean,
            default: false
        }
    },

    computed: {
        className: function() {
            const ls = [
                "tui",
                "tui-button"
            ];
            if (this.primary) {
                ls.push("tui-button-primary");
            }
            return ls;
        }
    }

};
</script>
